// Register GraphQL fields for ACF
add_action('graphql_register_types', 'vibemake_register_acf_graphql_fields');
function vibemake_register_acf_graphql_fields() {
    // Ensure ACF fields are exposed in GraphQL
    if (function_exists('register_graphql_field')) {
        // Add acfNewsFields to Post type
        register_graphql_field('Post', 'acfNewsFields', [
            'type' => 'NewsFields',
            'description' => 'ACF fields for news articles',
            'resolve' => function($post) {
                return [
                    'articleCategoryBadge' => get_field('article_category_badge', $post->ID),
                    'publicationDateOverride' => get_field('publication_date_override', $post->ID),
                ];
            }
        ]);

        // Register NewsFields type
        register_graphql_object_type('NewsFields', [
            'description' => 'ACF fields for news articles',
            'fields' => [
                'articleCategoryBadge' => ['type' => 'String'],
                'publicationDateOverride' => ['type' => 'String'],
            ],
        ]);

        // Add featured fields to Maker type
        register_graphql_field('Maker', 'featuredMaker', [
            'type' => 'Boolean',
            'description' => 'Whether this maker is featured in the homepage slider',
            'resolve' => function($maker) {
                return (bool) get_field('featured_maker', $maker->ID);
            }
        ]);

        register_graphql_field('Maker', 'featuredPosition', [
            'type' => 'Int',
            'description' => 'Position of this maker in the homepage slider (1-10)',
            'resolve' => function($maker) {
                return (int) get_field('featured_position', $maker->ID);
            }
        ]);

        // Add image fields to Maker type
        register_graphql_field('Maker', 'profileImage', [
            'type' => 'MediaItem',
            'description' => 'Profile image of the maker',
            'resolve' => function($maker, $args, $context, $info) {
                $image_id = get_field('profile_image', $maker->ID);
                if (!$image_id) {
                    return null;
                }
                return $context->get_loader('post')->load_deferred($image_id);
            }
        ]);

        register_graphql_field('Maker', 'backgroundImage', [
            'type' => 'MediaItem',
            'description' => 'Background image for the maker profile',
            'resolve' => function($maker, $args, $context, $info) {
                $image_id = get_field('background_image', $maker->ID);
                if (!$image_id) {
                    return null;
                }
                return $context->get_loader('post')->load_deferred($image_id);
            }
        ]);

        // Add other Maker fields
